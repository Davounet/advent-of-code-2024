function walkThePath(input) {
  let status = 'walking'
  let iterations = 0
  const state = structuredClone(input)
  const lastTurns = []
  const guard = {
    x: state.find(r => r.includes('^')).indexOf('^'),
    y: state.indexOf(state.find(r => r.includes('^'))),
    o: 'top'
  }
  state[guard.y][guard.x] = 'X'

  while (true) {
    const next = { x: guard.x + orientations[guard.o][0], y: guard.y + orientations[guard.o][1] }

    if (next.x < 0 || next.x > limits.x || next.y < 0 || next.y > limits.y) {
      // Out of the board
      status = 'out'
      break
    }

    const piece = state[next.y][next.x]
    iterations++

    if (piece === '#' || piece === 'B') {
      // Switching orientation
      guard.o = order[(order.indexOf(guard.o) + 1) % 4]

      // We are in a loop
      if (lastTurns.find(turn => ['x', 'y', 'o'].every(k => turn[k] === guard[k]))) {
        status = 'loop'
        break
      }

      // Add the turn to the list
      lastTurns.push({ x: guard.x, y: guard.y, o: guard.o })
      if (lastTurns.length > maxTurns) lastTurns.shift()
    } else {
      // Moving in the current direction
      guard.x = next.x
      guard.y = next.y
      state[guard.y][guard.x] = 'X'
    }
  }

  return [state, status]
}

